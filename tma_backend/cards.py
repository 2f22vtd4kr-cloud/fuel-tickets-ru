def _build_pool() -> list[Card]:
    """Build weighted pool for random.choices()."""
    pool: list[Card] = []
    for card in CATALOG:
        w = RARITY_WEIGHTS.get(card.rarity, 100)
        pool.append(card)
    return pool


_POOL = _build_pool()
_WEIGHTS = [RARITY_WEIGHTS.get(c.rarity, 100) for c in _POOL]


def draw_cards(n: int = 5) -> list[Card]:
    """
    Draw n unique cards using weighted random sampling without replacement.
    Cards are de-duped by name so the same card won't appear twice in one draw.
    """
    drawn: list[Card] = []
    seen_names: set[str] = set()
    pool = list(zip(_POOL, _WEIGHTS))
    random.shuffle(pool)

    attempts = 0
    while len(drawn) < n and attempts < 500:
        cards_only  = [c for c, _ in pool]
        weights_only = [w for _, w in pool]
        pick = random.choices(cards_only, weights=weights_only, k=1)[0]
        if pick.name not in seen_names:
            drawn.append(pick)
            seen_names.add(pick.name)
        attempts += 1

    return drawn
